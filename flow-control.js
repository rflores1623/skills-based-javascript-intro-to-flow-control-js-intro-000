function basicTeenager(age) {
	if ( age >= 13 && age <= 19) {
		return "You are a teenager!"
	}

	if ( age >= 13) {
		return undefined;
	}
}

basicTeenager();


function teenager(age) {
	if ( age >= 13 && age <= 19) {
		return "You are a teenager!";
	} else {
		return "You are not a teenager";
	}
}

teenager();
